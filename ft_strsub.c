#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*str;
	int		i;

	i = 0;
	if (!(str = ft_strnew(len)))
		return (NULL);
	while (len > 0)
	{
		str[i] = s[start];
		len--;
		start++;
		i++;
	}
	return (str);
}
