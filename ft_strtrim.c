#include "libft.h"

char	*ft_strtrim(char const *s)
{
	int i;

	i = 0;
	while (*s == ' ' || *s == '\n' || *s == '\t')
		s++;
	if (*s != '\0')
		i = (ft_strlen(s) - 1);
	while (s[i] == ' ' || s[i] == '\n' || s[i] == '\t')
		i--;
	return (ft_strsub(s, 0, i + 1));
}
