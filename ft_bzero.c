#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int				i;
	unsigned char	*tmp;

	i = 0;
	tmp = s;
	while (n > 0)
	{
		tmp[i] = '\0';
		i++;
		n--;
	}
}
